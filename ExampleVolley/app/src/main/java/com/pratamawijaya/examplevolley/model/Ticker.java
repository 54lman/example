public class Ticker {
    private String high;
    private String low;
    private String vol_btc;
    private String vol_idr;
    private String last;
    private String buy;
    private String sell;
    private String server_time;

    public String getHigh() {
        return high;
    }

    public String getLow() {
        return low;
    }

    public String getVol_btc() {
        return vol_btc;
    }

    public String getVol_idr() {
        return vol_idr;
    }

    public String getLast() {
        return last;
    }

    public String getBuy() {
        return buy;
    }

    public String getSell() {
        return sell;
    }

    public String getServer_time() {
        return server_time;
    }
}
